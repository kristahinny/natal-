// Função para mostrar a foto
function mostrarFoto() {
    // Obtenha a div que contém a foto
    var foto = document.getElementById("foto");

    // Altere o estilo para exibir a foto
    foto.style.display = "block";
}

// Função para ocultar a foto
function ocultarFoto() {
    // Obtenha a div que contém a foto
    var foto = document.getElementById("foto");

    // Altere o estilo para esconder a foto
    foto.style.display = "none";
}
